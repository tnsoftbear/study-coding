#include <iostream>

/**
 * Слишком далекий выход за границу массива приводит к Segmentation fault.
 * Пример показывает, что не слишком далёкий выход за границу массива не приводит к RE (это рассматирвается как UB).
 */

int main() {
    int arr[100];
    for (int i = 0; i < 100000; i++) {
        //arr[i] = i;
        std::cout << i << ":\t\t" << arr[i] << std::endl;
    }
}